import SwiftUI
import PhotosUI
import FoundationModels

struct CreateIssueView: View {
    let owner: String
    let repo: String
    let issueService: IssueService
    let attachmentService: AttachmentService
    let onCreated: () -> Void

    @Environment(\.dismiss) private var dismiss
    @AppStorage("quickMentionText") private var quickMentionText = "@claude"
    @AppStorage("issueTitlePrompt") private var issueTitlePrompt = "Generate a concise issue title (maximum 10 words) for this bug report or feature request.\nRespond with only the title text, no quotes, prefixes, or explanation."

    @State private var title = ""
    @State private var descriptionText = ""
    @State private var isSubmitting = false
    @State private var isGeneratingTitle = false
    @State private var errorMessage: String?
    @State private var showError = false

    // Attachment state
    @State private var selectedPhotos: [PhotosPickerItem] = []
    @State private var pendingAttachments: [PendingAttachment] = []
    @State private var isLoadingPhotos = false

    // Dictation state
    @State private var dictationManager = DictationManager()
    @State private var showDictationPermissionAlert = false
    @State private var textBeforeDictation = ""
    @State private var cursorPositionAtDictationStart: Int = 0
    @State private var selectedRange: NSRange?

    private var canGenerateTitle: Bool {
        SystemLanguageModel.default.isAvailable && descriptionText.count > 20 && !isGeneratingTitle
    }

    var body: some View {
        #if targetEnvironment(macCatalyst)
        macOSLayout
        #else
        iOSLayout
        #endif
    }

    // MARK: - macOS Layout

    private var macOSLayout: some View {
        VStack(spacing: 0) {
            // Header
            HStack {
                Text("New Issue")
                    .font(.title2)
                    .fontWeight(.semibold)
                Spacer()
            }
            .padding(.horizontal, 24)
            .padding(.top, 24)
            .padding(.bottom, 16)

            Divider()

            // Form content
            ScrollView {
                VStack(alignment: .leading, spacing: 24) {
                    // Title section
                    VStack(alignment: .leading, spacing: 8) {
                        Text("Title")
                            .font(.headline)
                            .foregroundStyle(.secondary)
                        HStack(spacing: 8) {
                            TextField("Issue title (autogenerated when empty)", text: $title)
                                .textFieldStyle(.roundedBorder)
                            if SystemLanguageModel.default.isAvailable {
                                Button {
                                    Task { await generateTitle() }
                                } label: {
                                    if isGeneratingTitle {
                                        ProgressView()
                                            .controlSize(.small)
                                    } else {
                                        Image(systemName: "sparkles")
                                    }
                                }
                                .buttonStyle(.bordered)
                                .disabled(!canGenerateTitle)
                                .help("Generate title from description")
                            }
                        }
                    }

                    // Description section
                    VStack(alignment: .leading, spacing: 8) {
                        HStack {
                            Text("Description")
                                .font(.headline)
                                .foregroundStyle(.secondary)

                            Spacer()

                            Button {
                                insertMention()
                            } label: {
                                Text(quickMentionText)
                                    .font(.subheadline)
                            }
                            .buttonStyle(.bordered)
                            .controlSize(.small)
                        }
                        ZStack(alignment: .bottomTrailing) {
                            TextEditorWithSelection(text: $descriptionText, selectedRange: $selectedRange)
                                .frame(minHeight: 200)
                                .overlay(
                                    RoundedRectangle(cornerRadius: 6)
                                        .stroke(Color(uiColor: .separator), lineWidth: 1)
                                )

                            // Dictation button
                            Button {
                                Task { await handleDictation() }
                            } label: {
                                Image(systemName: dictationManager.isRecording ? "mic.fill" : "mic")
                                    .font(.title3)
                                    .foregroundStyle(dictationManager.isRecording ? .red : .secondary)
                                    .symbolEffect(.pulse, isActive: dictationManager.isRecording)
                                    .frame(width: 44, height: 44)
                                    .contentShape(Rectangle())
                            }
                            .buttonStyle(.plain)
                            .padding(4)
                        }
                    }

                    // Attachments section
                    VStack(alignment: .leading, spacing: 8) {
                        Text("Attachments")
                            .font(.headline)
                            .foregroundStyle(.secondary)

                        if pendingAttachments.isEmpty {
                            PhotosPicker(
                                selection: $selectedPhotos,
                                maxSelectionCount: 10,
                                matching: .any(of: [.images, .screenshots])
                            ) {
                                SwiftUI.Label("Add Images", systemImage: "photo.on.rectangle.angled")
                            }
                            .buttonStyle(.bordered)
                        } else {
                            HStack(alignment: .top, spacing: 8) {
                                LazyVGrid(columns: [GridItem(.adaptive(minimum: 72), spacing: 8)], alignment: .leading, spacing: 8) {
                                    ForEach(pendingAttachments) { attachment in
                                        PendingAttachmentThumbnailView(attachment: attachment) {
                                            pendingAttachments.removeAll { $0.id == attachment.id }
                                        }
                                    }
                                }

                                Spacer()

                                PhotosPicker(
                                    selection: $selectedPhotos,
                                    maxSelectionCount: 10,
                                    matching: .any(of: [.images, .screenshots])
                                ) {
                                    Image(systemName: "plus")
                                        .font(.title3)
                                        .frame(width: 72, height: 72)
                                        .background(Color(uiColor: .tertiarySystemFill))
                                        .clipShape(RoundedRectangle(cornerRadius: 8))
                                }
                                .buttonStyle(.plain)
                            }
                        }

                        if isLoadingPhotos {
                            HStack {
                                ProgressView()
                                    .controlSize(.small)
                                Text("Loading photos...")
                                    .font(.caption)
                                    .foregroundStyle(.secondary)
                            }
                        }
                    }
                }
                .padding(24)
            }

            Divider()

            // Footer with buttons
            HStack {
                Spacer()
                Button("Cancel") {
                    dismiss()
                }
                .buttonStyle(.bordered)
                .keyboardShortcut(.cancelAction)

                Button("Create Issue") {
                    Task { await createIssue() }
                }
                .buttonStyle(.borderedProminent)
                .keyboardShortcut(.defaultAction)
                .disabled((title.isEmpty && descriptionText.count <= 20) || isSubmitting)
            }
            .padding(20)
            .background(.ultraThinMaterial)
        }
        .frame(minWidth: 500, minHeight: 450)
        .alert("Error", isPresented: $showError) {
            Button("OK", role: .cancel) { }
        } message: {
            Text(errorMessage ?? "Unknown error")
        }
        .onChange(of: selectedPhotos) { _, newValue in
            if !newValue.isEmpty {
                Task { await loadSelectedPhotos() }
            }
        }
    }

